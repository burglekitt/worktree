#!/usr/bin/env node
/**
 * build-context.mjs
 *
 * Reads all docs MDX files + the root SKILL.md and generates
 * docs/worker/docs-context.ts which exports a
 * single SYSTEM_PROMPT string that is embedded in the worker at
 * build time.
 *
 * Run: pnpm --filter docs worker:build-context
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const docsRoot = join(__dirname, "..");
const repoRoot = join(docsRoot, "..");
const outFile = join(__dirname, "docs-context.ts");
const routesOutFile = join(docsRoot, "src/chat/docs-routes.generated.ts");

// ── Helpers ──────────────────────────────────────────────────────────────────

/** Recursively find all files matching a suffix under a directory. */
function findFiles(dir, suffix) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...findFiles(full, suffix));
    } else if (entry.name.endsWith(suffix)) {
      results.push(full);
    }
  }
  return results.sort();
}

function stripMdx(raw) {
  return (
    raw
      // Remove import/export statements (MDX JSX imports)
      .replace(/^(import|export)\s+.*$/gm, "")
      // Remove JSX tags like <Cards>, <Card ...>, </Cards> etc.
      .replace(/<[A-Z][^>]*\/>/g, "")
      .replace(/<[A-Z][^>]*>[\s\S]*?<\/[A-Z][^>]*>/g, "")
      // Remove inline JSX expressions {something}
      .replace(/\{[^}]*\}/g, "")
      // Collapse multiple blank lines to one
      .replace(/\n{3,}/g, "\n\n")
      .trim()
  );
}

function readMdxFiles(subdir, rootLabel) {
  const absDir = join(docsRoot, subdir);
  const files = findFiles(absDir, ".mdx");
  const sections = [];
  for (const f of files) {
    const raw = readFileSync(f, "utf8");
    const text = stripMdx(raw);
    if (text.length < 10) continue;
    const label = relative(docsRoot, f).replace(/\\/g, "/");
    sections.push(`### ${rootLabel}: ${label}\n\n${text}`);
  }
  return sections.join("\n\n---\n\n");
}

/**
 * Derive the set of valid doc routes from MDX files on disk.
 * src/app/docs/commands/branch/page.mdx  →  /docs/commands/branch
 * src/app/docs/commands/page.mdx         →  /docs/commands
 * src/app/docs/page.mdx                  →  /docs
 */
function deriveDocRoutes(subdir) {
  const absDir = join(docsRoot, subdir);
  const files = findFiles(absDir, ".mdx");
  const routes = new Set();
  for (const f of files) {
    // Relative to src/app inside docsRoot, e.g. "docs/commands/branch/page.mdx"
    const rel = relative(join(docsRoot, "src/app"), f).replace(/\\/g, "/");
    // Strip trailing /page.mdx to get the route segment
    const route = `/${rel.replace(/\/page\.mdx$/, "").replace(/\.mdx$/, "")}`;
    routes.add(route);
  }
  return Array.from(routes).sort();
}

// ── Load docs MDX ─────────────────────────────────────────────────────────────

const docsContent = readMdxFiles("src/app/docs", "Docs");

// ── Derive valid doc routes from the same MDX scan ───────────────────────────

const VALID_DOC_ROUTES = deriveDocRoutes("src/app/docs");

// ── Write generated routes file for the Next.js app ──────────────────────────

const routesOutput = `// AUTO-GENERATED by docs/worker/build-context.mjs — do not edit manually
// Re-run: pnpm --filter docs worker:build-context

export const VALID_DOC_ROUTES: ReadonlySet<string> = new Set([
${VALID_DOC_ROUTES.map((r) => `  ${JSON.stringify(r)},`).join("\n")}
]);
`;
writeFileSync(routesOutFile, routesOutput, "utf8");
console.log(
  `✓ Wrote docs-routes.generated.ts (${VALID_DOC_ROUTES.length} routes)`,
);

const skillPath = join(repoRoot, "skills/core/SKILL.md");
const skillContent = existsSync(skillPath)
  ? readFileSync(skillPath, "utf8")
  : "(skills/core/SKILL.md not found — run npx @tanstack/intent@latest validate)";

// ── Assemble system prompt ────────────────────────────────────────────────────

// These must stay in sync with the actual Next.js app/docs directory structure.
const routeList = VALID_DOC_ROUTES.map((r) => `  ${r}`).join("\n");

const SYSTEM_PROMPT =
  `You are a helpful assistant for the Worktree CLI documentation site.
Answer questions based only on the documentation and skill guide below.
Be concise, accurate, and cite the relevant section when helpful.
If a question is outside this scope, say so politely and suggest the docs.

## Linking rules — follow exactly

- The docs site is served at https://burglekitt.github.io/worktree
- When linking to a docs page, use ONLY the relative path (e.g. [branch command](/docs/commands/branch)).
- ONLY link to routes from the list below. Do NOT invent routes, do NOT link to GitHub repo anchors like https://github.com/burglekitt/worktree#section, and do NOT link to headings inside the Skill Guide.
- If a concept you want to reference has no matching route below, mention it in plain text without a link.

Valid doc routes:
${routeList}

## Worktree CLI Skill Guide

${skillContent}

## Documentation

${docsContent}
`.trim();

// ── Write output ──────────────────────────────────────────────────────────────

const output = `// AUTO-GENERATED by docs/scripts/build-context.mjs — do not edit manually
// Re-run: pnpm --filter docs worker:build-context

export const SYSTEM_PROMPT = ${JSON.stringify(SYSTEM_PROMPT)};
`;

writeFileSync(outFile, output, "utf8");
console.log(
  `✓ Wrote docs-context.ts (${(output.length / 1024).toFixed(1)} KB)`,
);
console.log(
  `  Docs sections: ${(docsContent.match(/^### Docs:/gm) || []).length}`,
);
console.log(
  `  Skill guide: ${existsSync(skillPath) ? "✓ found" : "⚠ missing (SKILL.md not yet created)"}`,
);
