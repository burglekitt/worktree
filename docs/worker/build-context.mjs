#!/usr/bin/env node
/**
 * build-context.mjs
 *
 * Reads all docs MDX files + the root SKILL.md and generates
 * docs/worker/docs-context.ts which exports a
 * single SYSTEM_PROMPT string that is embedded in the worker at
 * build time.
 *
 * Run: pnpm --filter docs worker:build-context
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const docsRoot = join(__dirname, "..");
const repoRoot = join(docsRoot, "..");
const outFile = join(__dirname, "docs-context.ts");

// ── Helpers ──────────────────────────────────────────────────────────────────

/** Recursively find all files matching a suffix under a directory. */
function findFiles(dir, suffix) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...findFiles(full, suffix));
    } else if (entry.name.endsWith(suffix)) {
      results.push(full);
    }
  }
  return results.sort();
}

function stripMdx(raw) {
  return (
    raw
      // Remove import/export statements (MDX JSX imports)
      .replace(/^(import|export)\s+.*$/gm, "")
      // Remove JSX tags like <Cards>, <Card ...>, </Cards> etc.
      .replace(/<[A-Z][^>]*\/>/g, "")
      .replace(/<[A-Z][^>]*>[\s\S]*?<\/[A-Z][^>]*>/g, "")
      // Remove inline JSX expressions {something}
      .replace(/\{[^}]*\}/g, "")
      // Collapse multiple blank lines to one
      .replace(/\n{3,}/g, "\n\n")
      .trim()
  );
}

function readMdxFiles(subdir, rootLabel) {
  const absDir = join(docsRoot, subdir);
  const files = findFiles(absDir, ".mdx");
  const sections = [];
  for (const f of files) {
    const raw = readFileSync(f, "utf8");
    const text = stripMdx(raw);
    if (text.length < 10) continue;
    const label = relative(docsRoot, f).replace(/\\/g, "/");
    sections.push(`### ${rootLabel}: ${label}\n\n${text}`);
  }
  return sections.join("\n\n---\n\n");
}

// ── Load docs MDX ─────────────────────────────────────────────────────────────

const docsContent = readMdxFiles("src/app/docs", "Docs");

// ── Load SKILL.md from skills/core (TanStack Intent canonical location) ───────

const skillPath = join(repoRoot, "skills/core/SKILL.md");
const skillContent = existsSync(skillPath)
  ? readFileSync(skillPath, "utf8")
  : "(skills/core/SKILL.md not found — run npx @tanstack/intent@latest validate)";

// ── Assemble system prompt ────────────────────────────────────────────────────

const SYSTEM_PROMPT =
  `You are a helpful assistant for the Worktree CLI documentation site.
Answer questions based only on the documentation and skill guide below.
Be concise, accurate, and cite the relevant section when helpful.
If a question is outside this scope, say so politely and suggest the docs.

## Worktree CLI Skill Guide

${skillContent}

## Documentation

${docsContent}
`.trim();

// ── Write output ──────────────────────────────────────────────────────────────

const output = `// AUTO-GENERATED by docs/scripts/build-context.mjs — do not edit manually
// Re-run: pnpm --filter docs worker:build-context

export const SYSTEM_PROMPT = ${JSON.stringify(SYSTEM_PROMPT)};
`;

writeFileSync(outFile, output, "utf8");
console.log(
  `✓ Wrote docs-context.ts (${(output.length / 1024).toFixed(1)} KB)`,
);
console.log(
  `  Docs sections: ${(docsContent.match(/^### Docs:/gm) || []).length}`,
);
console.log(
  `  Skill guide: ${existsSync(skillPath) ? "✓ found" : "⚠ missing (SKILL.md not yet created)"}`,
);
